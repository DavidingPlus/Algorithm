# 符合的月日只有 0111, 0222, 1011, 1101, 1110, 1112, 1113, 1114, 1115, 1116, 1117, 1118, 1119, 1121, 1211, 1222
# 根据上面，枚举记录每一个对应的时分的可取的个数
# 这个枚举其实非常简单，因为规定了格式是 3 + 1，因此只有 4 中情况，非常好排除
counts = [4, 4, 4, 4, 4, 4, 3, 3, 3, 2, 2, 2, 2, 4, 4, 4]
# 年份都是可以取的，因此每个都是 4
ans = 0
for i in range(len(counts)):
    ans += 4 * counts[i]
print(ans)
